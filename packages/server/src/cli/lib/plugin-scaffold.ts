function frontendPageTemplate(parsed: ParsedPluginId): string {
  return `import { useState } from 'react';
import { usePluginRuntime } from '@uruc/plugin-sdk/frontend-react';

export function PluginPage() {
  const runtime = usePluginRuntime();
  const [result, setResult] = useState('');
  const [error, setError] = useState('');

  const runPing = async () => {
    setError('');
    try {
      const payload = await runtime.sendCommand<{ message: string }>('${parsed.pluginId}.${parsed.commandId}@v1');
      setResult(payload.message);
    } catch (err) {
      setError(err instanceof Error ? err.message : 'Command failed.');
    }
  };

  return (
    <div className="page-wrap main-grid">
      <section className="card control-section">
        <h1 className="title-card">${parsed.displayName} Hub</h1>
        <p className="section-sub">A minimal generated plugin page for ${parsed.pluginId}.</p>
        <p className="muted">Current location: {runtime.currentLocation ?? 'outside city'}</p>
        <div className="row" style={{ gap: 12, marginTop: 16 }}>
          <button type="button" className="btn primary" onClick={() => { void runPing(); }}>
            Run ping
          </button>
        </div>
        {result ? <div className="notice success" style={{ marginTop: 16 }}>{result}</div> : null}
        {error ? <div className="notice error" style={{ marginTop: 16 }}>{error}</div> : null}
      </section>
    </div>
  );
}
`;
}

function readmeTemplate(parsed: ParsedPluginId, withFrontend: boolean): string {
  return `# ${parsed.displayName}

Generated by \`uruc plugin create\`.

## Package

- Plugin ID: \`${parsed.pluginId}\`
- Package name: \`${parsed.packageName}\`
- Frontend entry: ${withFrontend ? '`enabled`' : '`disabled`'}

## Install

\`\`\`bash
cd packages/server
../../scripts/uruc.mjs configure
../../scripts/uruc.mjs plugin link <path-to-this-plugin>
../../scripts/uruc.mjs start
\`\`\`

## SDK imports

- Backend: \`@uruc/plugin-sdk/backend\`
- Frontend protocol: \`@uruc/plugin-sdk/frontend\`
- Frontend React helpers: \`@uruc/plugin-sdk/frontend-react\`
- Frontend HTTP helper: \`@uruc/plugin-sdk/frontend-http\`
`;
}

export async function createPluginScaffold(options: CreatePluginScaffoldOptions): Promise<{
  pluginId: string;
  packageName: string;
  targetDir: string;
}> {
  const parsed = assertValidPluginId(options.pluginId);
  const targetDir = path.resolve(options.targetDir);

  await mkdir(path.dirname(targetDir), { recursive: true });
  await mkdir(targetDir, { recursive: false });
  await writeFile(path.join(targetDir, 'package.json'), packageJsonTemplate(parsed, options.withFrontend), 'utf8');
  await writeFile(path.join(targetDir, 'index.mjs'), backendTemplate(parsed), 'utf8');
  await writeFile(path.join(targetDir, 'README.md'), readmeTemplate(parsed, options.withFrontend), 'utf8');

  if (options.withFrontend) {
    const frontendDir = path.join(targetDir, 'frontend');
    await mkdir(frontendDir, { recursive: true });
    await writeFile(path.join(frontendDir, 'plugin.ts'), frontendPluginTemplate(parsed), 'utf8');
    await writeFile(path.join(frontendDir, 'PluginPage.tsx'), frontendPageTemplate(parsed), 'utf8');
  }

  return {
    pluginId: parsed.pluginId,
    packageName: parsed.packageName,
    targetDir,
  };
}

export function defaultPluginScaffoldDir(serverPackageRoot: string, pluginId: string): string {
  const parsed = assertValidPluginId(pluginId);
  return path.resolve(serverPackageRoot, '..', 'plugins', parsed.directorySlug);
}
