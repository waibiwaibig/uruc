import { mkdir, writeFile } from 'fs/promises';
import path from 'path';

interface CreatePluginScaffoldOptions {
  pluginId: string;
  targetDir: string;
  withFrontend: boolean;
}

interface ParsedPluginId {
  pluginId: string;
  publisher: string;
  segments: string[];
  packageName: string;
  directorySlug: string;
  displayName: string;
  locationId: string;
  routeId: string;
  commandId: string;
}

function assertValidPluginId(pluginId: string): ParsedPluginId {
  const trimmed = pluginId.trim();
  if (!/^[a-z0-9-]+(?:\.[a-z0-9-]+)+$/.test(trimmed)) {
    throw new Error('Plugin id must be a lowercase namespaced id like acme.demo');
  }

  const segments = trimmed.split('.');
  const [publisher, ...rest] = segments;
  const featureSlug = rest.join('-');
  const words = featureSlug.split('-').filter(Boolean);
  const displayName = words.map((part) => part.charAt(0).toUpperCase() + part.slice(1)).join(' ');

  return {
    pluginId: trimmed,
    publisher,
    segments: rest,
    packageName: `@${publisher}/plugin-${featureSlug}`,
    directorySlug: segments.join('-'),
    displayName,
    locationId: `${featureSlug}-hub`,
    routeId: 'home',
    commandId: 'ping',
  };
}

function packageJsonTemplate(parsed: ParsedPluginId, withFrontend: boolean): string {
  const pkg: Record<string, unknown> = {
    name: parsed.packageName,
    private: true,
    version: '0.1.0',
    type: 'module',
    exports: {
      '.': './index.mjs',
    },
    urucPlugin: {
      pluginId: parsed.pluginId,
      apiVersion: 2,
      kind: 'backend',
      entry: './index.mjs',
      publisher: parsed.publisher,
      displayName: parsed.displayName,
      description: `${parsed.displayName} venue module generated by uruc plugin create.`,
      venue: {
        moduleId: parsed.pluginId,
        namespace: parsed.pluginId,
        displayName: parsed.displayName,
        description: `${parsed.displayName} venue module.`,
      },
      permissions: [],
      dependencies: [],
      activation: ['startup'],
    },
  };

  if (withFrontend) {
    pkg.urucFrontend = {
      apiVersion: 1,
      entry: './frontend/plugin.ts',
    };
  }

  return `${JSON.stringify(pkg, null, 2)}\n`;
}

function backendTemplate(parsed: ParsedPluginId): string {
  return `import { defineBackendPlugin } from '@uruc/plugin-sdk/backend';

const PLUGIN_ID = '${parsed.pluginId}';
const LOCATION_ID = '${parsed.locationId}';

export default defineBackendPlugin({
  pluginId: PLUGIN_ID,
  async setup(ctx) {
    await ctx.locations.register({
      id: LOCATION_ID,
      name: '${parsed.displayName} Hub',
      description: 'A generated V2 plugin location.',
    });

    await ctx.commands.register({
      id: '${parsed.commandId}',
      description: 'Return a minimal generated status payload.',
      inputSchema: {},
      controlPolicy: {
        controllerRequired: false,
      },
      locationPolicy: {
        scope: 'any',
      },
      handler: async (_input, runtimeCtx) => ({
        ok: true,
        pluginId: PLUGIN_ID,
        locationId: runtimeCtx.currentLocation ?? null,
        message: '${parsed.displayName} is online.',
      }),
    });
  },
});
`;
}

